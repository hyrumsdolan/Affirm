const { Schema, model } = require('mongoose');

const entrySchema = new Schema({
  title: {
    type: String,
    required: true,
    trim: true,
  },
  content: {
    type: String,
    required: true,
  },
  createdAt: {
    type: String,
    required: true,
  },
  updatedAt: {
    type: String,
    required: true,
  },
});

const Entry = model('Entry', entrySchema);

module.exports = Entry;